	public void drawMaze(){
		
		for (int i = 0; i < col; i++) {
			for (int j = 0; j < row; j++) {
				if(matrix[j][i] == 0) {
					app.fill(255);
				}else if (matrix[j][i] == 1) {
					app.fill(0);
				}else {
					app.fill(255,0,0);
				}
				rect((i*40),(j*40),40,40);
			}
		}
		
		app.ellipse(posX,posY,25,25);
	}

	public void mousePressed() {
	
	}
	
	public void keyPressed() {
		switch (keyCode) {
		case RIGHT:
			if(matrix[matY][matX+1]!=1) {
				posX += 40;
				matX++;
			}
			break;
		case LEFT:
			if(matrix[matY][matX-1]!=1) {
				posX -= 40;
				matX--;
			}
			break;
		case UP:
			if(matrix[matY-1][matX]!=1) {
				posY -= 40;
				matY--;
			}
			break;
		case DOWN:
			if(matrix[matY+1][matX]!=1) {
				posY += 40;
				matY++;
			}
			break;
		default:
			break;
		}
	}
